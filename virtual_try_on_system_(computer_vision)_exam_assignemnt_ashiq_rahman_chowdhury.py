def load_local_pipeline():
    global local_pipe
    if local_pipe is not None:
        return local_pipe

    from diffusers import StableDiffusionInpaintPipeline
    print('Loading local SD inpainting pipeline (~5GB download)...')
    local_pipe = StableDiffusionInpaintPipeline.from_pretrained(
        'stabilityai/stable-diffusion-2-inpainting',
        torch_dtype=torch.float16 if device == 'cuda' else torch.float32
    ).to(device)
    local_pipe.enable_attention_slicing()  # memory optimization
    print('✅ Local pipeline loaded!')
    return local_pipe


def inpaint_local(
    image:  Image.Image,
    mask:   Image.Image,
    prompt: str,
    negative_prompt: str = 'blurry, distorted, deformed, low quality',
    num_steps: int = 30,
    guidance_scale: float = 7.5
) -> Image.Image:
    pipe = load_local_pipeline()
    image = image.resize((512, 512))
    mask  = mask.resize((512, 512))

    print(f'🚀 Running local inpainting — {num_steps} steps...')
    result = pipe(
        prompt=prompt,
        negative_prompt=negative_prompt,
        image=image,
        mask_image=mask,
        num_inference_steps=num_steps,
        guidance_scale=guidance_scale
    ).images[0]
    print('✅ Local inpainting complete!')
    return result


# -------------------------------------------------------
# Fallback: Simulation (no API / no GPU)
# -------------------------------------------------------
def inpaint_simulation(
    image:      Image.Image,
    mask:       Image.Image,
    prompt:     str,
    body_part:  str
) -> Image.Image:
    """
    Fallback simulation: color-blend the masked region
    based on keywords in the prompt.
    """
    color_map = {
        'black':  (30,  30,  30),
        'white':  (240, 240, 240),
        'red':    (192, 57,  43),
        'blue':   (41,  128, 185),
        'denim':  (58,  90,  137),
        'navy':   (26,  35,  126),
        'green':  (39,  174, 96),
        'grey':   (127, 140, 141),
        'gray':   (127, 140, 141),
        'yellow': (241, 196, 15),
        'pink':   (232, 67,  147),
        'purple': (142, 68,  173),
        'brown':  (121, 85,  72),
        'khaki':  (195, 176, 145),
        'orange': (230, 126, 34),
    }

    p_lower = prompt.lower()
    color = (100, 120, 180)  # default blue-grey
    for key, val in color_map.items():
        if key in p_lower:
            color = val
            break

    img_arr  = np.array(image.convert('RGB')).astype(float)
    mask_arr = np.array(mask.convert('L')).astype(float) / 255.0

    # Blend color into masked region
    result = img_arr.copy()
    for c, val in enumerate(color):
        result[:,:,c] = img_arr[:,:,c] * (1 - mask_arr * 0.7) + val * mask_arr * 0.7

    result_img = Image.fromarray(result.astype(np.uint8))

    # Add watermark
    draw = ImageDraw.Draw(result_img)
    draw.rectangle([0, result_img.height-26, result_img.width, result_img.height],
                   fill=(0, 0, 0, 180))
    draw.text((8, result_img.height-18),
              f'[SIMULATION] {prompt[:40]} | Add HF token for real results',
              fill=(255, 255, 100))

    return result_img


