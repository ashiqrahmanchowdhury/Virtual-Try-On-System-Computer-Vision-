"""## Segmentation Function"""

def segment_body_part(image: Image.Image, body_part: str) -> dict:
    """
    Segment upper or lower body clothing from image.

    Args:
        image     : PIL Image (preprocessed)
        body_part : 'upper' or 'lower'

    Returns:
        dict with:
          - 'mask'         : binary PIL Image (white=clothing, black=background)
          - 'segmap'       : full segmentation map array
          - 'labels_found' : list of detected clothing labels
          - 'coverage_pct' : % of image covered by the region
    """
    target_labels = UPPER_LABELS if body_part == 'upper' else LOWER_LABELS

    # Run SegFormer inference
    inputs = processor(images=image, return_tensors='pt').to(device)

    with torch.no_grad():
        outputs = seg_model(**inputs)

    # Upsample logits to original image size
    logits = outputs.logits  # shape: (1, num_labels, H/4, W/4)
    upsampled = torch.nn.functional.interpolate(
        logits,
        size=image.size[::-1],  # (H, W)
        mode='bilinear',
        align_corners=False
    )
    seg_map = upsampled.argmax(dim=1).squeeze().cpu().numpy()  # (H, W)

    # Build binary mask for target clothing labels
    binary_mask = np.zeros_like(seg_map, dtype=np.uint8)
    labels_found = []

    for label_id in target_labels:
        region = (seg_map == label_id)
        if region.sum() > 0:
            binary_mask[region] = 255
            labels_found.append(LABEL_MAP[label_id])

    # Dilate mask slightly for better inpainting coverage
    if binary_mask.sum() > 0:
        dilated = binary_dilation(binary_mask > 0, iterations=8).astype(np.uint8) * 255
        binary_mask = dilated

    # Smooth mask edges
    mask_pil = Image.fromarray(binary_mask)
    mask_pil = mask_pil.filter(ImageFilter.GaussianBlur(radius=3))
    mask_arr = np.array(mask_pil)
    mask_arr = (mask_arr > 127).astype(np.uint8) * 255
    mask_pil = Image.fromarray(mask_arr)

    total_pixels = seg_map.size
    coverage = (binary_mask > 0).sum() / total_pixels * 100

    return {
        'mask':          mask_pil,
        'segmap':        seg_map,
        'labels_found':  labels_found,
        'coverage_pct':  round(coverage, 1)
    }


