HF_API_URL = 'https://api-inference.huggingface.co/models/stabilityai/stable-diffusion-2-inpainting'

def image_to_bytes(image: Image.Image, fmt: str = 'PNG') -> bytes:
    buf = io.BytesIO()
    image.save(buf, format=fmt)
    return buf.getvalue()


def inpaint_with_hf_api(
    image:  Image.Image,
    mask:   Image.Image,
    prompt: str,
    token:  str,
    negative_prompt: str = 'blurry, distorted, deformed, low quality, ugly',
    num_inference_steps: int = 30,
    guidance_scale: float = 7.5
) -> Image.Image:
    """
    Call HF Inference API for inpainting.
    Uses multipart/form-data with image + mask + prompt.
    """
    print(f'🚀 Calling HF Inpainting API...')
    print(f'   Prompt: "{prompt}"')

    # Ensure both are 512x512
    image = image.resize((512, 512))
    mask  = mask.resize((512, 512))

    # Build request payload
    payload = {
        'inputs': prompt,
        'parameters': {
            'negative_prompt': negative_prompt,
            'num_inference_steps': num_inference_steps,
            'guidance_scale': guidance_scale
        }
    }

    files = {
        'image': ('image.png', image_to_bytes(image), 'image/png'),
        'mask_image': ('mask.png', image_to_bytes(mask), 'image/png'),
    }

    headers = {'Authorization': f'Bearer {token}'}

    # Some HF models use JSON payload only
    import base64
    def b64(img):
        return base64.b64encode(image_to_bytes(img)).decode()

    json_payload = {
        'inputs': prompt,
        'parameters': {
            'negative_prompt': negative_prompt,
            'num_inference_steps': num_inference_steps,
            'guidance_scale': guidance_scale,
            'image': b64(image),
            'mask_image': b64(mask)
        }
    }

    response = requests.post(
        HF_API_URL,
        headers=headers,
        json=json_payload,
        timeout=120
    )

    if response.status_code == 200:
        content_type = response.headers.get('content-type', '')
        if 'image' in content_type:
            result = Image.open(io.BytesIO(response.content))
            print(f'✅ Inpainting complete!')
            return result
        else:
            # Model might be loading
            data = response.json()
            raise RuntimeError(f'API returned non-image: {data}')

    elif response.status_code == 503:
        import time
        data = response.json()
        wait = data.get('estimated_time', 20)
        print(f'   Model loading, waiting {wait:.0f}s...')
        time.sleep(wait + 5)
        return inpaint_with_hf_api(image, mask, prompt, token, negative_prompt, num_inference_steps, guidance_scale)

    else:
        raise RuntimeError(f'HF API error {response.status_code}: {response.text[:200]}')


