def virtual_tryon_system(
    image_input,
    body_part:   str   = 'upper',
    prompt:      str   = '',
    hf_token:    str   = '',
    use_local:   bool  = False,
    show_steps:  bool  = True
) -> dict:
    """
    Full Virtual Try-On System.

    Args:
        image_input : PIL Image or file path
        body_part   : 'upper' or 'lower'
        prompt      : clothing description (e.g. 'black leather jacket')
        hf_token    : Hugging Face API token (optional)
        use_local   : use local diffusers instead of API
        show_steps  : display intermediate results

    Returns:
        dict with 'result', 'mask', 'segmap', 'labels_found'
    """
    print('='*55)
    print(f' 👗 VIRTUAL TRY-ON SYSTEM')
    print(f'    Body part : {body_part.upper()}')
    print(f'    Prompt    : "{prompt}"')
    print('='*55)

    # --- Load image ---
    if isinstance(image_input, str):
        image = Image.open(image_input).convert('RGB')
    elif isinstance(image_input, np.ndarray):
        image = Image.fromarray(image_input)
    else:
        image = image_input.convert('RGB')

    # --- Step 1: Preprocess ---
    print('\n[1/4] Preprocessing image...')
    image = preprocess_image(image, target_size=512)
    show_image_info(image, 'Preprocessed')

    # --- Step 2: Segment ---
    print(f'\n[2/4] Segmenting {body_part} body clothing...')
    seg_result = segment_body_part(image, body_part)
    print(f'      Labels found : {seg_result["labels_found"] or ["None"]}')
    print(f'      Coverage     : {seg_result["coverage_pct"]}%')

    if show_steps:
        visualize_segmentation(image, seg_result, body_part)

    mask = seg_result['mask']

    # Warn if nothing detected
    if not seg_result['labels_found']:
        print(f'\n⚠️  No {body_part} clothing detected.')
        print('    Falling back to region-based mask...')
        # Simple fallback: use top or bottom half
        mask_arr = np.zeros((512, 512), dtype=np.uint8)
        if body_part == 'upper':
            mask_arr[80:300, 100:412] = 255
        else:
            mask_arr[300:480, 80:430] = 255
        mask = Image.fromarray(mask_arr)

    # Set default prompt
    if not prompt.strip():
        prompt = 'a stylish outfit, high quality fashion photo' if body_part == 'upper' \
                 else 'stylish pants, high quality fashion photo'

    full_prompt = f'photo of a person wearing {prompt}, photorealistic, high quality, fashion photo'

    # --- Step 3: Inpaint ---
    print(f'\n[3/4] Running AI inpainting...')
    print(f'      Full prompt: "{full_prompt}"')

    token = hf_token or HF_TOKEN

    if use_local:
        result = inpaint_local(image, mask, full_prompt)
    elif token:
        try:
            result = inpaint_with_hf_api(image, mask, full_prompt, token)
        except Exception as e:
            print(f'      ⚠️  HF API failed: {e}')
            print('      Falling back to simulation...')
            result = inpaint_simulation(image, mask, prompt, body_part)
    else:
        print('      ⚠️  No token — using simulation mode')
        result = inpaint_simulation(image, mask, prompt, body_part)

    result = result.resize((512, 512))

    # --- Step 4: Display ---
    print('\n[4/4] Displaying final result...')
    if show_steps:
        fig, axes = plt.subplots(1, 3, figsize=(15, 5))
        fig.suptitle('Virtual Try-On — Final Output', fontsize=14, fontweight='bold')

        axes[0].imshow(image)
        axes[0].set_title('Original', fontweight='bold')
        axes[0].axis('off')

        axes[1].imshow(mask, cmap='gray')
        axes[1].set_title(f'{body_part.title()} Body Mask', fontweight='bold')
        axes[1].axis('off')

        axes[2].imshow(result)
        axes[2].set_title(f'Result: "{prompt[:30]}"', fontweight='bold')
        axes[2].axis('off')

        plt.tight_layout()
        plt.savefig('/content/tryon_result.png', dpi=150, bbox_inches='tight')
        plt.show()
        print('💾 Result saved to /content/tryon_result.png')

    print('\n✅ Pipeline complete!')

    return {
        'result':       result,
        'mask':         mask,
        'original':     image,
        'segmap':       seg_result['segmap'],
        'labels_found': seg_result['labels_found'],
        'coverage_pct': seg_result['coverage_pct']
    }


