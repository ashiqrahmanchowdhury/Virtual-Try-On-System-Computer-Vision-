def preprocess_image(image: Image.Image, target_size: int = 512) -> Image.Image:
    """
    Preprocess image:
    - Convert to RGB
    - Resize (maintain aspect ratio, pad to square)
    - Normalize
    """
    # Convert to RGB
    image = image.convert('RGB')

    # Resize maintaining aspect ratio
    w, h = image.size
    ratio = target_size / max(w, h)
    new_w = int(w * ratio)
    new_h = int(h * ratio)
    image = image.resize((new_w, new_h), Image.LANCZOS)

    # Pad to square
    square = Image.new('RGB', (target_size, target_size), (255, 255, 255))
    pad_x = (target_size - new_w) // 2
    pad_y = (target_size - new_h) // 2
    square.paste(image, (pad_x, pad_y))

    return square


