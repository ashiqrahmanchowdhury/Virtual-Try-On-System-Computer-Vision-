    try:
        result = virtual_tryon_system(
            image_input=image,
            body_part=body_part.lower(),
            prompt=prompt,
            hf_token="",   # 👉 always empty = auto mode
            show_steps=False
        )

        status_lines = [
            '✅ Done!',
            f'Body part  : {body_part}',
            f'Clothing   : {prompt}',
            f'Labels found: {", ".join(result["labels_found"]) or "None (fallback mask used)"}',
            f'Coverage   : {result["coverage_pct"]}%',
            '🟡 Auto mode (no HF token required)'
        ]

        return (
            np.array(result['result']),
            np.array(result['mask'].convert('RGB')),
            '\n'.join(status_lines)
        )

    except Exception as e:
        import traceback
        return None, None, f'❌ Error: {str(e)}\n\n{traceback.format_exc()}'


